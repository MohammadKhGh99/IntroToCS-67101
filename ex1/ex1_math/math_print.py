import math
def golden_ratio():
    """this code prints the golden ratio"""
    print((1+math.sqrt(5))/2)

def six_cubed():
    """this code prints the 6 powered by 3"""
    print(math.pow(6,3))

def hypotenuse():
    """this code prints the hypotenuse of a triangle"""
    math.hypot(3,5)

def pi():
    """this code prints the value of pi"""
    print(math.pi)

def e():
    """this code prints the value of e"""
    print(math.e)

def triangular_area():
    """this code prints the areas of 10 angular triangles"""
    print(1*1/2,2*2/2,3*3/2,4*4/2,5*5/2,6*6/2,7*7/2,8*8/2,9*9/2,10*10/2)

if __name__ == "__main__":
    golden_ratio()
    six_cubed()
    hypotenuse()
    pi()
    e()
    triangular_area()